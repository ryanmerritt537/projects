package summerProject;

public class ForStatements {
	public static void main(String args[]) {
		// makes a bunch of for loops
		for (int i = 1; i <= 20; i++) {
			// loops 20 times
		}
		for (int icount = 1; icount <= 20; icount += 2) {
			// loops 10 times
		}
		for (int j = 1; j <= 100; j += 5) {
			// loops 20 times
		}
		for (int icount = 20; icount >= 1; icount += -1) {
			// loops 20 times
		}
		for (int icount = 20; icount >= 1; icount += -2) {
			// loops 10 times
		}
		for (double xcnt = 20; xcnt >= 9.5; xcnt += -0.05) {
			// loops 210 times
		}
	}
}
